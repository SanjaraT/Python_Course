print("--PROGRAM TO FIND OUT THE GREATER NUMBER--")
a = int(input("Enter the first number : "))
b = int(input("Enter the second number : "))
if (a>b):
    print("True : the first number is greater than the second number.")
elif (b>a):
    print("False : the second number is greater than the first number.")
else: 
    print("None")

